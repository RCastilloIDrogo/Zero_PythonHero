#Aqui hay detalle, en esta parte al parecer si nuestro numero es un "str" y queremos hacer 
#La conversion se va tomar el mismo "str" como numero

x = int(1)   # x will be 1
y = int(2.8) # y will be 2
z = int("3") # z will be 3

x1 = float(1)   # x will be 1.0
y1 = float(2.8) # y will be 2.8
z1 = float("3") # z will be 3.0

x2 = str(1)   # x will be 1
y2 = str(2.8) # y will be 2
z2 = str("3") # z will be 3

print("----INTEGER----")
print(x)
print(y)
print(z)

print("----FLOAT----")
print(x1)
print(y1)
print(z1)

print("----STRING----")
print(x2)
print(y2)
print(z2)