'''Las tuples asi como las listas se pueden unir, en este caso lo podemos hacer
haciendo uso del operador "+" '''

tuple1 = ("Mondong", "Ceviche", "Pescado")
tuple2 = (23, 34, 45)
tupleuni = tuple1 + tuple2
tuplemul = tuple2 * 5 # Ademas podemos multiplicar un tuple con la finalidad de repetir los valores de la misma
#Considerando el numero que se puede colocar

print("La union de ambas tuplas es: ", tupleuni)
print("La multiplicacion por dos de la tuples es: ", tuplemul)
