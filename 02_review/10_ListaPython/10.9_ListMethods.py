'''Ahora mismo vamos a ver todos los metodos que tiene python 
incorporados que se pueden usar en las listas'''

#append() | Agrega un elemento al final de la lista
#clear() | Elimina todo elemenos en la lista
#copy() | Devuelve una copia de la lista seleccionada
#extend() | Agrega los elementos de una lista a otra podriamos decir
#index() | Devuelve el indice del primer elemento con valor especifico
#insert()| Agrega un elemento en la posicion especificada
#pop() | Elimina el elemento en la posicion especificada
#remove() | Elimina un valor seleccionado
#reverse() | Invierte el orden de la lista
#sort() | Ordena la lista (De forma ascendente o descendente)