#Modificando la "Tipografia" de un STR

print("---- UPER CASE ----")#Mayusculas
a = "Hello, World Freeman"
print(a.upper())

print("---- LOWER CASE ----")#Minusculas
b = "Hello, Gordon Gordon"
print(b.lower())

print("---- REMOVE WHITESCAPE ----")#Remueve los espacios si es que los hay al imprimir
c = "Hello, World Pepingo"
print(c.strip())

print("---- REPLACE STRING ----")#Cambia Letra o Palabra Seleccionada
d = "Hello, Hello Haaaa"
print(d.replace("H", "HAHAHa"))

print("---- SPLIT STRING ----")#Separador
e = "Hola, Mundo"
print(e.split(","))