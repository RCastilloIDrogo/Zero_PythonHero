#Ahora mismo lo que vamos hacer es entender como funciona el "while" y el "for"
#   while | Se usa cuando quieres repetir cierta regla pero hasta que se cumpla
#   for | Se usa mayormente cuando quieres repetir algo cierta cantidad de veces

print("----- while -----")
item = 1
while item <= 10:
    print(item)
    item += 1

