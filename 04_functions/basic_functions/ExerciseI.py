#Que es una funcion? "def"
#Es un bloque de codigo qque:
#Tiene nombre | Puede ser llamado | Definido un vez, pero a su vez poder llamarlo varias veces

#Ahora mismo quiero trabajarlo con una clase, porque pinto

class Funtion():

    def nombre_de_funcion(self):
        print("Hola! Bienvenido de nuevo")
    
    def linea(self):
        print("=" * 40)

    def mostrar_nombre(self):
        print("Mi nombre es Gordon Freeman")
    
#Creamos un objeto
funcion = Funtion()

#Llamar a los metodos
funcion.nombre_de_funcion()
funcion.linea()
funcion.mostrar_nombre()



